<?php


namespace Profit\Nordea\API\Parser\TITORecords;


abstract class Record implements RecordInterface
{
    protected $data;

    /**
     * @return mixed
     */
    public function getData()
    {
        return $this->data;
    }

    /**
     * @param mixed $data
     */
    public function setData($data)
    {
        $this->data = $data;
    }
}